use crate::framework::{
    array_heap::ArrayHeap,
    class_file::ClassFile,
    frame::Frame,
    heap::StringTable,
    native::NativeMethodHandler,
    object_heap::ObjectHeap,
    types::{JvmError, Value},
};

#[allow(clippy::too_many_arguments)]
pub fn execute(
    classes: &[ClassFile],
    strings: &mut StringTable,
    objects: &mut ObjectHeap,
    arrays: &mut ArrayHeap,
    handler: &mut impl NativeMethodHandler,
    class_idx: usize,
    method_idx: usize,
    args: &[Value],
) -> Result<Option<Value>, JvmError> {
    let mut frame = Frame::new(class_idx, method_idx, args)?;

    loop {
        let cf = &classes[frame.class_idx];
        let method = &cf.methods[frame.method_idx];
        let code = cf.method_code(method);

        if frame.pc >= code.len() {
            return Ok(None);
        }

        let opcode = code[frame.pc];
        frame.pc += 1;

        match opcode {
            // nop
            0x00 => {}

            // aconst_null
            0x01 => frame.push(Value::Null)?,

            // iconst_<n>: -1..5
            0x02 => frame.push(Value::Int(-1))?,
            0x03 => frame.push(Value::Int(0))?,
            0x04 => frame.push(Value::Int(1))?,
            0x05 => frame.push(Value::Int(2))?,
            0x06 => frame.push(Value::Int(3))?,
            0x07 => frame.push(Value::Int(4))?,
            0x08 => frame.push(Value::Int(5))?,

            // bipush
            0x10 => {
                let b = code[frame.pc] as i8;
                frame.pc += 1;
                frame.push(Value::Int(b as i32))?;
            }

            // sipush
            0x11 => {
                let hi = code[frame.pc] as i16;
                let lo = code[frame.pc + 1] as i16;
                frame.pc += 2;
                frame.push(Value::Int(((hi << 8) | lo) as i32))?;
            }

            // ldc
            0x12 => {
                let cp_idx = code[frame.pc] as u16;
                frame.pc += 1;
                let v = resolve_ldc(cf, strings, cp_idx)?;
                frame.push(v)?;
            }

            // ldc_w
            0x13 => {
                let cp_idx = u16::from_be_bytes([code[frame.pc], code[frame.pc + 1]]);
                frame.pc += 2;
                let v = resolve_ldc(cf, strings, cp_idx)?;
                frame.push(v)?;
            }

            // iload (index u8)
            0x15 => {
                let idx = code[frame.pc];
                frame.pc += 1;
                let v = frame.load_local(idx)?;
                frame.push(v)?;
            }

            // aload (index u8)
            0x19 => {
                let idx = code[frame.pc];
                frame.pc += 1;
                let v = frame.load_local(idx)?;
                frame.push(v)?;
            }

            // iload_<n>
            0x1a => {
                let v = frame.load_local(0)?;
                frame.push(v)?;
            }
            0x1b => {
                let v = frame.load_local(1)?;
                frame.push(v)?;
            }
            0x1c => {
                let v = frame.load_local(2)?;
                frame.push(v)?;
            }
            0x1d => {
                let v = frame.load_local(3)?;
                frame.push(v)?;
            }

            // aload_<n>
            0x2a => {
                let v = frame.load_local(0)?;
                frame.push(v)?;
            }
            0x2b => {
                let v = frame.load_local(1)?;
                frame.push(v)?;
            }
            0x2c => {
                let v = frame.load_local(2)?;
                frame.push(v)?;
            }
            0x2d => {
                let v = frame.load_local(3)?;
                frame.push(v)?;
            }

            // istore (index u8)
            0x36 => {
                let idx = code[frame.pc];
                frame.pc += 1;
                let v = frame.pop()?;
                frame.store_local(idx, v)?;
            }

            // astore (index u8)
            0x3a => {
                let idx = code[frame.pc];
                frame.pc += 1;
                let v = frame.pop()?;
                frame.store_local(idx, v)?;
            }

            // istore_<n>
            0x3b => {
                let v = frame.pop()?;
                frame.store_local(0, v)?;
            }
            0x3c => {
                let v = frame.pop()?;
                frame.store_local(1, v)?;
            }
            0x3d => {
                let v = frame.pop()?;
                frame.store_local(2, v)?;
            }
            0x3e => {
                let v = frame.pop()?;
                frame.store_local(3, v)?;
            }

            // astore_<n>
            0x4b => {
                let v = frame.pop()?;
                frame.store_local(0, v)?;
            }
            0x4c => {
                let v = frame.pop()?;
                frame.store_local(1, v)?;
            }
            0x4d => {
                let v = frame.pop()?;
                frame.store_local(2, v)?;
            }
            0x4e => {
                let v = frame.pop()?;
                frame.store_local(3, v)?;
            }

            // pop
            0x57 => {
                frame.pop()?;
            }

            // pop2
            0x58 => {
                frame.pop()?;
                frame.pop()?;
            }

            // dup
            0x59 => {
                let v = frame.pop()?;
                frame.push(v)?;
                frame.push(v)?;
            }

            // iadd
            0x60 => {
                let b = frame.pop()?;
                let a = frame.pop()?;
                match (a, b) {
                    (Value::Int(a), Value::Int(b)) => frame.push(Value::Int(a.wrapping_add(b)))?,
                    _ => return Err(JvmError::InvalidBytecode),
                }
            }

            // isub
            0x64 => {
                let b = frame.pop()?;
                let a = frame.pop()?;
                match (a, b) {
                    (Value::Int(a), Value::Int(b)) => frame.push(Value::Int(a.wrapping_sub(b)))?,
                    _ => return Err(JvmError::InvalidBytecode),
                }
            }

            // imul
            0x68 => {
                let b = frame.pop()?;
                let a = frame.pop()?;
                match (a, b) {
                    (Value::Int(a), Value::Int(b)) => frame.push(Value::Int(a.wrapping_mul(b)))?,
                    _ => return Err(JvmError::InvalidBytecode),
                }
            }

            // idiv
            0x6c => {
                let b = frame.pop()?;
                let a = frame.pop()?;
                match (a, b) {
                    (Value::Int(_), Value::Int(0)) => return Err(JvmError::InvalidBytecode),
                    (Value::Int(a), Value::Int(b)) => frame.push(Value::Int(a.wrapping_div(b)))?,
                    _ => return Err(JvmError::InvalidBytecode),
                }
            }

            // irem
            0x70 => {
                let b = frame.pop()?;
                let a = frame.pop()?;
                match (a, b) {
                    (Value::Int(_), Value::Int(0)) => return Err(JvmError::InvalidBytecode),
                    (Value::Int(a), Value::Int(b)) => frame.push(Value::Int(a.wrapping_rem(b)))?,
                    _ => return Err(JvmError::InvalidBytecode),
                }
            }

            // ineg
            0x74 => {
                let v = frame.pop()?;
                match v {
                    Value::Int(n) => frame.push(Value::Int(n.wrapping_neg()))?,
                    _ => return Err(JvmError::InvalidBytecode),
                }
            }

            // ishl
            0x78 => {
                let b = frame.pop()?;
                let a = frame.pop()?;
                match (a, b) {
                    (Value::Int(a), Value::Int(b)) => {
                        frame.push(Value::Int(a.wrapping_shl((b & 0x1f) as u32)))?
                    }
                    _ => return Err(JvmError::InvalidBytecode),
                }
            }

            // ishr
            0x7a => {
                let b = frame.pop()?;
                let a = frame.pop()?;
                match (a, b) {
                    (Value::Int(a), Value::Int(b)) => {
                        frame.push(Value::Int(a.wrapping_shr((b & 0x1f) as u32)))?
                    }
                    _ => return Err(JvmError::InvalidBytecode),
                }
            }

            // iushr
            0x7c => {
                let b = frame.pop()?;
                let a = frame.pop()?;
                match (a, b) {
                    (Value::Int(a), Value::Int(b)) => frame.push(Value::Int(
                        ((a as u32).wrapping_shr((b & 0x1f) as u32)) as i32,
                    ))?,
                    _ => return Err(JvmError::InvalidBytecode),
                }
            }

            // iand
            0x7e => {
                let b = frame.pop()?;
                let a = frame.pop()?;
                match (a, b) {
                    (Value::Int(a), Value::Int(b)) => frame.push(Value::Int(a & b))?,
                    _ => return Err(JvmError::InvalidBytecode),
                }
            }

            // ior
            0x80 => {
                let b = frame.pop()?;
                let a = frame.pop()?;
                match (a, b) {
                    (Value::Int(a), Value::Int(b)) => frame.push(Value::Int(a | b))?,
                    _ => return Err(JvmError::InvalidBytecode),
                }
            }

            // ixor
            0x82 => {
                let b = frame.pop()?;
                let a = frame.pop()?;
                match (a, b) {
                    (Value::Int(a), Value::Int(b)) => frame.push(Value::Int(a ^ b))?,
                    _ => return Err(JvmError::InvalidBytecode),
                }
            }

            // iinc: local[index] += const
            0x84 => {
                let idx = code[frame.pc];
                let inc = code[frame.pc + 1] as i8;
                frame.pc += 2;
                if let Value::Int(n) = frame.load_local(idx)? {
                    frame.store_local(idx, Value::Int(n.wrapping_add(inc as i32)))?;
                } else {
                    return Err(JvmError::InvalidBytecode);
                }
            }

            // ifeq: branch if TOS == 0
            0x99 => {
                let offset = i16::from_be_bytes([code[frame.pc], code[frame.pc + 1]]);
                frame.pc += 2;
                let v = frame.pop()?;
                if matches!(v, Value::Int(0) | Value::Null) {
                    frame.pc = branch_target(frame.pc, offset);
                }
            }

            // ifne: branch if TOS != 0
            0x9a => {
                let offset = i16::from_be_bytes([code[frame.pc], code[frame.pc + 1]]);
                frame.pc += 2;
                let v = frame.pop()?;
                if !matches!(v, Value::Int(0) | Value::Null) {
                    frame.pc = branch_target(frame.pc, offset);
                }
            }

            // iflt
            0x9b => {
                let offset = i16::from_be_bytes([code[frame.pc], code[frame.pc + 1]]);
                frame.pc += 2;
                if let Value::Int(n) = frame.pop()? {
                    if n < 0 {
                        frame.pc = branch_target(frame.pc, offset);
                    }
                }
            }

            // ifge
            0x9c => {
                let offset = i16::from_be_bytes([code[frame.pc], code[frame.pc + 1]]);
                frame.pc += 2;
                if let Value::Int(n) = frame.pop()? {
                    if n >= 0 {
                        frame.pc = branch_target(frame.pc, offset);
                    }
                }
            }

            // ifgt
            0x9d => {
                let offset = i16::from_be_bytes([code[frame.pc], code[frame.pc + 1]]);
                frame.pc += 2;
                if let Value::Int(n) = frame.pop()? {
                    if n > 0 {
                        frame.pc = branch_target(frame.pc, offset);
                    }
                }
            }

            // ifle
            0x9e => {
                let offset = i16::from_be_bytes([code[frame.pc], code[frame.pc + 1]]);
                frame.pc += 2;
                if let Value::Int(n) = frame.pop()? {
                    if n <= 0 {
                        frame.pc = branch_target(frame.pc, offset);
                    }
                }
            }

            // if_icmpeq
            0x9f => {
                let offset = i16::from_be_bytes([code[frame.pc], code[frame.pc + 1]]);
                frame.pc += 2;
                let b = frame.pop()?;
                let a = frame.pop()?;
                if a == b {
                    frame.pc = branch_target(frame.pc, offset);
                }
            }

            // if_icmpne
            0xa0 => {
                let offset = i16::from_be_bytes([code[frame.pc], code[frame.pc + 1]]);
                frame.pc += 2;
                let b = frame.pop()?;
                let a = frame.pop()?;
                if a != b {
                    frame.pc = branch_target(frame.pc, offset);
                }
            }

            // if_icmplt: branch if a < b (a=below TOS, b=TOS)
            0xa1 => {
                let offset = i16::from_be_bytes([code[frame.pc], code[frame.pc + 1]]);
                frame.pc += 2;
                if let (Value::Int(b), Value::Int(a)) = (frame.pop()?, frame.pop()?) {
                    if a < b {
                        frame.pc = branch_target(frame.pc, offset);
                    }
                }
            }

            // if_icmpge
            0xa2 => {
                let offset = i16::from_be_bytes([code[frame.pc], code[frame.pc + 1]]);
                frame.pc += 2;
                if let (Value::Int(b), Value::Int(a)) = (frame.pop()?, frame.pop()?) {
                    if a >= b {
                        frame.pc = branch_target(frame.pc, offset);
                    }
                }
            }

            // if_icmpgt
            0xa3 => {
                let offset = i16::from_be_bytes([code[frame.pc], code[frame.pc + 1]]);
                frame.pc += 2;
                if let (Value::Int(b), Value::Int(a)) = (frame.pop()?, frame.pop()?) {
                    if a > b {
                        frame.pc = branch_target(frame.pc, offset);
                    }
                }
            }

            // if_icmple
            0xa4 => {
                let offset = i16::from_be_bytes([code[frame.pc], code[frame.pc + 1]]);
                frame.pc += 2;
                if let (Value::Int(b), Value::Int(a)) = (frame.pop()?, frame.pop()?) {
                    if a <= b {
                        frame.pc = branch_target(frame.pc, offset);
                    }
                }
            }

            // goto — signed 16-bit offset from opcode start (opcode is at pc-1)
            0xa7 => {
                let offset = i16::from_be_bytes([code[frame.pc], code[frame.pc + 1]]);
                // goto instruction starts at frame.pc - 1; offset is from that start
                frame.pc = ((frame.pc as i32) - 1 + offset as i32) as usize;
            }

            // ireturn
            0xac => {
                let v = frame.pop()?;
                return Ok(Some(v));
            }

            // areturn
            0xb0 => {
                let v = frame.pop()?;
                return Ok(Some(v));
            }

            // return (void)
            0xb1 => return Ok(None),

            // getstatic — for M2 we only see this for PeripheralManager; push Null placeholder
            0xb2 => {
                frame.pc += 2;
                frame.push(Value::Null)?;
            }

            // getfield — objectref → value
            0xb4 => {
                let _cp_idx = u16::from_be_bytes([code[frame.pc], code[frame.pc + 1]]);
                frame.pc += 2;
                let obj_ref = frame.pop()?;
                match obj_ref {
                    Value::ObjectRef(idx) => {
                        let v = objects.get_field(idx, 0).unwrap_or(Value::Null);
                        frame.push(v)?;
                    }
                    _ => return Err(JvmError::InvalidReference),
                }
            }

            // putfield — objectref, value →
            0xb5 => {
                let _cp_idx = u16::from_be_bytes([code[frame.pc], code[frame.pc + 1]]);
                frame.pc += 2;
                let value = frame.pop()?;
                let obj_ref = frame.pop()?;
                match obj_ref {
                    Value::ObjectRef(idx) => {
                        objects
                            .set_field(idx, 0, value)
                            .ok_or(JvmError::InvalidReference)?;
                    }
                    _ => return Err(JvmError::InvalidReference),
                }
            }

            // invokevirtual
            0xb6 => {
                let cp_idx = u16::from_be_bytes([code[frame.pc], code[frame.pc + 1]]);
                frame.pc += 2;
                let (class_bytes, name_bytes, desc_bytes) =
                    cf.cp_methodref(cp_idx).ok_or(JvmError::InvalidBytecode)?;
                let class_str =
                    core::str::from_utf8(class_bytes).map_err(|_| JvmError::InvalidBytecode)?;
                let name_str =
                    core::str::from_utf8(name_bytes).map_err(|_| JvmError::InvalidBytecode)?;
                let desc_str =
                    core::str::from_utf8(desc_bytes).map_err(|_| JvmError::InvalidBytecode)?;
                let arg_count = 1 + count_args(desc_str); // +1 for `this`
                let result = invoke_method(
                    classes,
                    strings,
                    objects,
                    arrays,
                    handler,
                    class_str,
                    name_str,
                    desc_str,
                    &mut frame.stack,
                    arg_count,
                )?;
                if let Some(v) = result {
                    frame.push(v)?;
                }
            }

            // invokespecial (constructor / private)
            0xb7 => {
                let cp_idx = u16::from_be_bytes([code[frame.pc], code[frame.pc + 1]]);
                frame.pc += 2;
                let (class_bytes, name_bytes, desc_bytes) =
                    cf.cp_methodref(cp_idx).ok_or(JvmError::InvalidBytecode)?;
                let class_str =
                    core::str::from_utf8(class_bytes).map_err(|_| JvmError::InvalidBytecode)?;
                let name_str =
                    core::str::from_utf8(name_bytes).map_err(|_| JvmError::InvalidBytecode)?;
                let desc_str =
                    core::str::from_utf8(desc_bytes).map_err(|_| JvmError::InvalidBytecode)?;
                let arg_count = 1 + count_args(desc_str); // +1 for `this`
                let result = invoke_method(
                    classes,
                    strings,
                    objects,
                    arrays,
                    handler,
                    class_str,
                    name_str,
                    desc_str,
                    &mut frame.stack,
                    arg_count,
                )?;
                if let Some(v) = result {
                    frame.push(v)?;
                }
            }

            // invokestatic
            0xb8 => {
                let cp_idx = u16::from_be_bytes([code[frame.pc], code[frame.pc + 1]]);
                frame.pc += 2;
                let (class_bytes, name_bytes, desc_bytes) =
                    cf.cp_methodref(cp_idx).ok_or(JvmError::InvalidBytecode)?;
                let class_str =
                    core::str::from_utf8(class_bytes).map_err(|_| JvmError::InvalidBytecode)?;
                let name_str =
                    core::str::from_utf8(name_bytes).map_err(|_| JvmError::InvalidBytecode)?;
                let desc_str =
                    core::str::from_utf8(desc_bytes).map_err(|_| JvmError::InvalidBytecode)?;
                let arg_count = count_args(desc_str);
                let result = invoke_method(
                    classes,
                    strings,
                    objects,
                    arrays,
                    handler,
                    class_str,
                    name_str,
                    desc_str,
                    &mut frame.stack,
                    arg_count,
                )?;
                if let Some(v) = result {
                    frame.push(v)?;
                }
            }

            // new — allocate object, push objectref
            0xbb => {
                let cp_idx = u16::from_be_bytes([code[frame.pc], code[frame.pc + 1]]);
                frame.pc += 2;
                let class_name = cf
                    .cp_class_name(cp_idx)
                    .and_then(|b| core::str::from_utf8(b).ok())
                    .ok_or(JvmError::InvalidBytecode)?;
                let static_name = class_name_to_static(class_name);
                let obj_idx = objects.alloc(static_name).ok_or(JvmError::StackOverflow)?;
                frame.push(Value::ObjectRef(obj_idx))?;
            }

            // i2b — int to byte (sign-extend lower 8 bits)
            0x91 => {
                let v = frame.pop()?;
                if let Value::Int(n) = v {
                    frame.push(Value::Int(n as i8 as i32))?;
                } else {
                    return Err(JvmError::InvalidBytecode);
                }
            }

            // i2c — int to char (zero-extend lower 16 bits)
            0x92 => {
                let v = frame.pop()?;
                if let Value::Int(n) = v {
                    frame.push(Value::Int(n as u16 as i32))?;
                } else {
                    return Err(JvmError::InvalidBytecode);
                }
            }

            // i2s — int to short (sign-extend lower 16 bits)
            0x93 => {
                let v = frame.pop()?;
                if let Value::Int(n) = v {
                    frame.push(Value::Int(n as i16 as i32))?;
                } else {
                    return Err(JvmError::InvalidBytecode);
                }
            }

            // aaload — load reference from reference array
            0x32 => {
                let index = match frame.pop()? {
                    Value::Int(i) => i,
                    _ => return Err(JvmError::InvalidBytecode),
                };
                let arr_idx = match frame.pop()? {
                    Value::ArrayRef(i) => i,
                    Value::Null => return Err(JvmError::InvalidReference),
                    _ => return Err(JvmError::InvalidBytecode),
                };
                if index < 0 {
                    return Err(JvmError::ArrayIndexOutOfBounds);
                }
                let raw = arrays
                    .load(arr_idx, index as usize)
                    .ok_or(JvmError::ArrayIndexOutOfBounds)?;
                let v = if raw == 0 {
                    Value::Null
                } else {
                    Value::ObjectRef(raw as u16)
                };
                frame.push(v)?;
            }

            // iaload — load int from int array
            0x2e => {
                let index = match frame.pop()? {
                    Value::Int(i) => i,
                    _ => return Err(JvmError::InvalidBytecode),
                };
                let arr_idx = match frame.pop()? {
                    Value::ArrayRef(i) => i,
                    Value::Null => return Err(JvmError::InvalidReference),
                    _ => return Err(JvmError::InvalidBytecode),
                };
                if index < 0 {
                    return Err(JvmError::ArrayIndexOutOfBounds);
                }
                let v = arrays
                    .load(arr_idx, index as usize)
                    .ok_or(JvmError::ArrayIndexOutOfBounds)?;
                frame.push(Value::Int(v))?;
            }

            // baload — load byte/boolean from array (sign-extend)
            0x33 => {
                let index = match frame.pop()? {
                    Value::Int(i) => i,
                    _ => return Err(JvmError::InvalidBytecode),
                };
                let arr_idx = match frame.pop()? {
                    Value::ArrayRef(i) => i,
                    Value::Null => return Err(JvmError::InvalidReference),
                    _ => return Err(JvmError::InvalidBytecode),
                };
                if index < 0 {
                    return Err(JvmError::ArrayIndexOutOfBounds);
                }
                let raw = arrays
                    .load(arr_idx, index as usize)
                    .ok_or(JvmError::ArrayIndexOutOfBounds)?;
                frame.push(Value::Int(raw as i8 as i32))?;
            }

            // caload — load char from array (zero-extend)
            0x34 => {
                let index = match frame.pop()? {
                    Value::Int(i) => i,
                    _ => return Err(JvmError::InvalidBytecode),
                };
                let arr_idx = match frame.pop()? {
                    Value::ArrayRef(i) => i,
                    Value::Null => return Err(JvmError::InvalidReference),
                    _ => return Err(JvmError::InvalidBytecode),
                };
                if index < 0 {
                    return Err(JvmError::ArrayIndexOutOfBounds);
                }
                let raw = arrays
                    .load(arr_idx, index as usize)
                    .ok_or(JvmError::ArrayIndexOutOfBounds)?;
                frame.push(Value::Int(raw as u16 as i32))?;
            }

            // saload — load short from array (sign-extend)
            0x35 => {
                let index = match frame.pop()? {
                    Value::Int(i) => i,
                    _ => return Err(JvmError::InvalidBytecode),
                };
                let arr_idx = match frame.pop()? {
                    Value::ArrayRef(i) => i,
                    Value::Null => return Err(JvmError::InvalidReference),
                    _ => return Err(JvmError::InvalidBytecode),
                };
                if index < 0 {
                    return Err(JvmError::ArrayIndexOutOfBounds);
                }
                let raw = arrays
                    .load(arr_idx, index as usize)
                    .ok_or(JvmError::ArrayIndexOutOfBounds)?;
                frame.push(Value::Int(raw as i16 as i32))?;
            }

            // aastore — store reference into reference array
            0x53 => {
                let value = frame.pop()?;
                let index = match frame.pop()? {
                    Value::Int(i) => i,
                    _ => return Err(JvmError::InvalidBytecode),
                };
                let arr_idx = match frame.pop()? {
                    Value::ArrayRef(i) => i,
                    Value::Null => return Err(JvmError::InvalidReference),
                    _ => return Err(JvmError::InvalidBytecode),
                };
                if index < 0 {
                    return Err(JvmError::ArrayIndexOutOfBounds);
                }
                let raw = match value {
                    Value::Null => 0i32,
                    Value::ObjectRef(i) => i as i32,
                    _ => return Err(JvmError::InvalidBytecode),
                };
                arrays
                    .store(arr_idx, index as usize, raw)
                    .ok_or(JvmError::ArrayIndexOutOfBounds)?;
            }

            // bastore — store byte/boolean into array (truncate to 8 bits)
            0x54 => {
                let value = match frame.pop()? {
                    Value::Int(v) => v,
                    _ => return Err(JvmError::InvalidBytecode),
                };
                let index = match frame.pop()? {
                    Value::Int(i) => i,
                    _ => return Err(JvmError::InvalidBytecode),
                };
                let arr_idx = match frame.pop()? {
                    Value::ArrayRef(i) => i,
                    Value::Null => return Err(JvmError::InvalidReference),
                    _ => return Err(JvmError::InvalidBytecode),
                };
                if index < 0 {
                    return Err(JvmError::ArrayIndexOutOfBounds);
                }
                arrays
                    .store(arr_idx, index as usize, value as i8 as i32)
                    .ok_or(JvmError::ArrayIndexOutOfBounds)?;
            }

            // castore — store char into array (truncate to 16 bits)
            0x55 => {
                let value = match frame.pop()? {
                    Value::Int(v) => v,
                    _ => return Err(JvmError::InvalidBytecode),
                };
                let index = match frame.pop()? {
                    Value::Int(i) => i,
                    _ => return Err(JvmError::InvalidBytecode),
                };
                let arr_idx = match frame.pop()? {
                    Value::ArrayRef(i) => i,
                    Value::Null => return Err(JvmError::InvalidReference),
                    _ => return Err(JvmError::InvalidBytecode),
                };
                if index < 0 {
                    return Err(JvmError::ArrayIndexOutOfBounds);
                }
                arrays
                    .store(arr_idx, index as usize, value as u16 as i32)
                    .ok_or(JvmError::ArrayIndexOutOfBounds)?;
            }

            // sastore — store short into array (truncate to 16 bits, sign-extended)
            0x56 => {
                let value = match frame.pop()? {
                    Value::Int(v) => v,
                    _ => return Err(JvmError::InvalidBytecode),
                };
                let index = match frame.pop()? {
                    Value::Int(i) => i,
                    _ => return Err(JvmError::InvalidBytecode),
                };
                let arr_idx = match frame.pop()? {
                    Value::ArrayRef(i) => i,
                    Value::Null => return Err(JvmError::InvalidReference),
                    _ => return Err(JvmError::InvalidBytecode),
                };
                if index < 0 {
                    return Err(JvmError::ArrayIndexOutOfBounds);
                }
                arrays
                    .store(arr_idx, index as usize, value as i16 as i32)
                    .ok_or(JvmError::ArrayIndexOutOfBounds)?;
            }

            // iastore — store int into int array
            0x4f => {
                let value = match frame.pop()? {
                    Value::Int(v) => v,
                    _ => return Err(JvmError::InvalidBytecode),
                };
                let index = match frame.pop()? {
                    Value::Int(i) => i,
                    _ => return Err(JvmError::InvalidBytecode),
                };
                let arr_idx = match frame.pop()? {
                    Value::ArrayRef(i) => i,
                    Value::Null => return Err(JvmError::InvalidReference),
                    _ => return Err(JvmError::InvalidBytecode),
                };
                if index < 0 {
                    return Err(JvmError::ArrayIndexOutOfBounds);
                }
                arrays
                    .store(arr_idx, index as usize, value)
                    .ok_or(JvmError::ArrayIndexOutOfBounds)?;
            }

            // newarray — create new primitive array
            0xbc => {
                let atype = code[frame.pc];
                frame.pc += 1;
                let count = match frame.pop()? {
                    Value::Int(n) => n,
                    _ => return Err(JvmError::InvalidBytecode),
                };
                if count < 0 {
                    return Err(JvmError::NegativeArraySize);
                }
                let arr_idx = arrays
                    .alloc(atype, count as u8)
                    .ok_or(JvmError::StackOverflow)?;
                frame.push(Value::ArrayRef(arr_idx))?;
            }

            // anewarray — create new reference array (class cp_idx consumed but ignored)
            0xbd => {
                frame.pc += 2; // skip cp_idx
                let count = match frame.pop()? {
                    Value::Int(n) => n,
                    _ => return Err(JvmError::InvalidBytecode),
                };
                if count < 0 {
                    return Err(JvmError::NegativeArraySize);
                }
                let arr_idx = arrays
                    .alloc(crate::framework::array_heap::ATYPE_REF, count as u8)
                    .ok_or(JvmError::StackOverflow)?;
                frame.push(Value::ArrayRef(arr_idx))?;
            }

            // arraylength — get length of array
            0xbe => {
                let arr_idx = match frame.pop()? {
                    Value::ArrayRef(i) => i,
                    Value::Null => return Err(JvmError::InvalidReference),
                    _ => return Err(JvmError::InvalidBytecode),
                };
                let len = arrays.length(arr_idx).ok_or(JvmError::InvalidReference)?;
                frame.push(Value::Int(len as i32))?;
            }

            // checkcast — no-op for M2
            0xc0 => {
                frame.pc += 2;
            }

            op => return Err(JvmError::UnsupportedOpcode(op)),
        }
    }
}

